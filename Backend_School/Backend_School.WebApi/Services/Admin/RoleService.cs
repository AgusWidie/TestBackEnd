        public async Task<IEnumerable<RoleResponse>> AddRole(RoleAddRequest parameter, CancellationToken cancellationToken)
        {
            using (IDbConnection connection = new MySql.Data.MySqlClient.MySqlConnection(_configuration.GetValue<string>("ConnectionStrings:DefaultConnection")))
            {
                if (connection.State == ConnectionState.Closed)
                {
                    connection.Open();
                }

                using (var transaction = connection.BeginTransaction(IsolationLevel.ReadUncommitted))
                {
                    IEnumerable<RoleResponse> DataRole = null;
                    try
                    {
                        parameter.TanggalBuat = DateTime.Now;
                        DataRole = await connection.QueryAsync<RoleResponse>(new CommandDefinition("sp_AddRole", parameter, commandType: CommandType.StoredProcedure, cancellationToken: cancellationToken));
